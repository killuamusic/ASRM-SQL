FROM RM1_PACKAGE
WHERE PACKAGE_VALUE BETWEEN '896604061680400000' AND '896604061680400009'
AND package_pool_id IN (63,64)
AND PACKAGE_STATUS NOT IN ('SIM ITEM ID','AVAILABLE','SP ITEM ID','AVAILABLE SP','PREACTIVATED')
GROUP BY PACKAGE_STATUS 

